    shift(map);
    for(int i = 0 ; i <59 ; i++)
  {
    for(int j = 0 ; j < 49 ; j++)
    {
        type = map[i][j];
        src.x = j*10;
        src.y = i*10;
        switch(type){
            case 0: TextureManager::Draw(water , NULL , &src);
            break;
            case 1: TextureManager::Draw(grass , NULL , &src);
            break;
        
        }
    }   
  }
