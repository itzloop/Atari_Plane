void Map::shift(int arr[][50])
{
    int temp;
    for(int i = row1 ; i < row1 + 12; i++)
    {

        for(int j = 6 ; j < 11 ; j++)
        {
            if(row2 == 59)
            {
                temp = arr[59][j];
                arr[59][j] = arr[0][j];
                arr[0][j] = temp;
            }
            else
            {
                temp = arr[row1][j];
                arr[row1][j] = arr[row2][j];
                arr[row2][j] = temp;
            }
        }
        row1++;
        row2++;
        if(row2 == 59)
        {
            row1 = 0;
            row2 = 1;
        }
    }

        


}


